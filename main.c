#include "get_next_line.h"

int	main(void)
{
	int fd = open("text.txt", O_RDONLY);
	for(int i = 0; i < 9; i++)
	{
		printf("\033[0;37mline %i : %s\n", i, get_next_line(fd));
		printf("\033[0;34m----------------------------------------------------------------------------------\n");
	}
	return (0);
}
